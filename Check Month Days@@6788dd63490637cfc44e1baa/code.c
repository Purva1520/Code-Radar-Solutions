#include <stdio.h>
int main()
{
    int n;
    scanf("%d",&n);
    if(n==1,3,5,7,8,10,12)
    {
        printf("31");
    }
    else if(n==4,6,9,11)
    {
        printf("30");
    }
    else if(n==2)
    {
        printf("28");
    }
    else
    {
        printf("Invalid month");
    }
return 0;
}